package com.jbms.testlibrary.calc

class Calculator {
    /**
     * number add
     * @param a - number one
     * @param b - number two
     * @return a + b number
     */
    fun add(a:Int, b:Int) : Int{
        return a + b
    }

    /**
     * number minus
     */
    fun minus(a:Int, b:Int) : Int{
        return a + b
    }

    /**
     * @suppress
     */
    fun gob(a:Int, b:Int) : Int{
        return a + b
    }

    /**
     * mod
     */
    private fun mod(a:Int, b:Int) : Int{
        return a + b
    }
}